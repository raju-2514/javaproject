
package practice.java;


public class Loopcode {
    //for loop
    public static void main(String[] args) {
        int i;
    for( i=0;i<10;i++)
    {
        System.out.print(" "+i);
    }
   //while 
   
   int j=10;
   while(j<20)
   {
       System.out.print(" "+j);
       j++;
   }
   
     int k=20;
   do
   {
     
       System.out.print(" "+k);
       k++;
   }while(k<=30);
    
    
    }
    
    
}
